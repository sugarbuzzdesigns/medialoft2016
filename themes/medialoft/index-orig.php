					<p>If Don Draper had made a right on Broadway instead of a sinister left on Madison Avenue, his career might have deviated significantly from the world of advertising.  For there was a wackier (yet tangentially connected) world populated by moonlighting Broadway actors, singers, set designers, composers, and lyricists all in thrall to the modern American corporation. It was the world of the industrial musical. For those unfamiliar with the term, an “industrial musical” is best described as a corporate sponsored event (such as a convention or sales meeting) that incorporates the trappings of a Broadway musical with the messages of the free enterprise system.</p>

					<p>As Media Loft celebrates its 40th year in business, it’s nice to know that our start coincided with what authors Steve Young and Sport Murphy describe as ‘the golden age of industrial musicals’ in their comprehensive history book “Everything’s Coming Up Profits.”</p>

