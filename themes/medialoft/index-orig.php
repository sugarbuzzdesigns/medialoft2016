					<p>Did you know that the same composer who wrote lyrics for “Fiddler on the Roof” also graced us with the industrial blockbuster known as “Ford-i-fy Your Future?”</p>

					<p>Never heard of it? How about this classic from American Standard’s 1969 Industrial “The Bathroom’s are Coming” entitled “Look at this Tub.” In this searing ballad, we learn why American Standard is needed now more than ever…</p>

					<blockquote>“Look at this tub! LOOK AT THIS TUB!
					It’s dangerous and certainly a hazard!
					It’s positively lower than substandard!
					Everything here is lower class,
					Why, I could slip, I could fall right on my…nose”</blockquote>

