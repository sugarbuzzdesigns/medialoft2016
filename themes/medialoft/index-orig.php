								<h2 class="title">AIGA Minnesota Portfolio 1-on-1 &reg;</h2>
							</div>
							<div class="info">
								<span class="category">Culture</span> / <span class="date">04.20.16</span>
							</div>
						</div>
					</div>
					<div class="full-bleed blog-header-bg" style="background-image:url(<?php echo IMG_DIR; ?>/blog/p1on1_header2.jpg);"></div>
				</a>
				<div class="article-content">
					<p>Design students know about design firms and ad agencies. But when 20 design students strolled
					into Media Loft as part of their three-stop <a href="http://www.aigaminnesota.org/event/portfolio-one-on-one/" target="_blank">AIGA Minnesota Portfolio 1 on 1 &reg;</a> studio tour, they for the
					most part had no idea what to expect – for good reason.</p>

					<p>While design is a critical part of every corporate meeting we produce, the under-the- radar nature
					of our industry means that often only attendees and our clients are exposed to our Broadway-
					meets-business events in auditoriums, arenas, beachfronts and many other places, all across the
					globe.</p>

					<p>During their time at Media Loft, students had the opportunity to glimpse beyond our curtains. We
					showed them a brief history of our company, examples of our work, and an opportunity to pose
					questions to a staff Designer, Creative Director, and company President.
					One of the questions was, “What do you look for in a new hire?” For students and young
					designers out there wondering how you can get noticed at Media Loft (or elsewhere), read on:</p>

					<div class="blog-entry-img">
						<img src="<?php echo IMG_DIR; ?>/blog/p1on1_Content1.jpg" alt="">
					</div>

					<strong>1. Stand out</strong>
					<p>First and foremost are strong design skills. We are looking for designers who have a unique
					creative approach. Does the applicant have passion for design and creative curiosity? A top
					candidate’s portfolio would include a range of creative including strong identity design, typography
					and dynamic use of composition and color. As a young designer, technical knowledge is less
					important than a portfolio that reflects a range of creative approaches. If you have not had
					extensive professional experience include a few conceptual pieces that reflects your design
					aspirations.</p>

					<strong>2. Know your audience</strong>
					<p>It’s easy to tell if an applicant has done their homework. Know your audience. Read the
					company’s website. Understand what they do and how your skills would be an asset to the
					company. Media Loft looks for potential employees who are excited about the diverse range of
					opportunities we have In-house. Ask good questions. Don’t be afraid to write them down ahead of
					time. Enthusiasm and genuine interest in what you can do for a potential employer (rather than
					what they can do for you) will go a long way.</p>

					<strong>3. Be professional</strong>
					<p>Being prompt reflects well on you and allows you a moment to collect your thoughts before your
					interview.
					Look smart, both in your appearance and in the presentation of your work.
					Always send a prompt thank you for the opportunity by email and or post. Be persistent, not pushy. Ask about the hiring process and potential next steps. If you are asked to follow up - do it.</p>

					<strong>4. Be informed</strong>
					<p>Informational interviews are a great way to talk to people in the industry with no pressure on either
					side. Do your research and contact companies that do the kind of work you aspire to. Most
					companies will be glad to meet with you, answer your questions and review your work.
					Informational interviews give you the opportunity to learn about potential employers and get
					helpful feedback on your work. Think of it as practice. Be sure to prepare as if it is a formal
					interview. If you make a great impression it might get you one. Good luck!	</p>

